namespace Walgelijk
{
    /// <summary>
    /// Wrap mode for textures
    /// </summary>
    public enum WrapMode
    {
        /// <summary>
        /// Extends the edge pixels
        /// </summary>
        Clamp,
        /// <summary>
        /// Repeats the UV
        /// </summary>
        Repeat,
        /// <summary>
        /// Mirrors the UV
        /// </summary>
        Mirror
    }
}
